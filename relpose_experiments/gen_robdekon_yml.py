# Port the v1 robdekon (UR5 + KUKA iiwa, 13 DoF) robot config to cuRobo v2.
# Reads the v1 yml, applies the v1->v2 field renames, writes the v2 config.
# Run in container (PyYAML available):
#   docker exec neural-sdf-v2 bash -c "cd ~/ws/neural_sdf/curobo_v2 && \
#       python relpose_experiments/gen_robdekon_yml.py"
from pathlib import Path

import yaml

# v1 robdekon config. Resolve across mount points (container: ~/ws/neural_sdf;
# host: dekon_scan/neural_sdf). relpose_experiments lives under curobo_v2/, so
# neural_sdf is a sibling two levels up.
V1_CANDIDATES = [
    "/home/yucheng/ws/neural_sdf/curobo/src/curobo/content/configs/robot/robdekon_demo_scanning.yml",
    str(Path(__file__).resolve().parents[2]
        / "neural_sdf/curobo/src/curobo/content/configs/robot/robdekon_demo_scanning.yml"),
]
ROOT = Path(__file__).resolve().parent.parent
DST = ROOT / "curobo/content/configs/robot/robdekon_scanning.yml"


def _find_v1():
    for p in V1_CANDIDATES:
        if Path(p).exists():
            return p
    raise FileNotFoundError(f"v1 robdekon config not found in {V1_CANDIDATES}")


def main():
    k = yaml.safe_load(open(_find_v1()))["robot_cfg"]["kinematics"]

    # v1 -> v2 field renames
    tool_frames = k.get("link_names") or [k["ee_link"]]
    cspace = dict(k["cspace"])
    cspace["default_joint_position"] = cspace.pop("retract_config")  # v2 name

    kin = {
        "format_version": 2.0,
        "asset_root_path": k["asset_root_path"],
        "urdf_path": k["urdf_path"],
        "base_link": k["base_link"],
        "tool_frames": tool_frames,  # replaces v1 ee_link + link_names
        "collision_link_names": k["collision_link_names"],
        "collision_sphere_buffer": k.get("collision_sphere_buffer", 0.005),
        "use_global_cumul": k.get("use_global_cumul", True),
        "mesh_link_names": None,
        "collision_spheres": k["collision_spheres"],
        "self_collision_ignore": {
            kk: vv for kk, vv in k["self_collision_ignore"].items()
        },
        "self_collision_buffer": k.get("self_collision_buffer", {}),
        "cspace": cspace,
    }
    # NOTE: load_dynamics is intentionally omitted — it is a RobotCfg-level field,
    # not a kinematics field, and defaults to False.
    out = {"robot_cfg": {"kinematics": kin}}
    with open(DST, "w") as f:
        f.write("## Auto-generated by relpose_experiments/gen_robdekon_yml.py (v1->v2 port)\n")
        f.write("## robdekon dual-arm: UR5 (6) + KUKA iiwa (7) = 13 DoF.\n")
        yaml.safe_dump(out, f, sort_keys=False, default_flow_style=None, width=120)
    print(f"wrote {DST}")
    print("tool_frames:", tool_frames)
    print("joints:", cspace["joint_names"])


if __name__ == "__main__":
    main()
