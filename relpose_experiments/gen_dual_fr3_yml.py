# Generate curobo/content/configs/robot/dual_fr3.yml from the single-Panda
# franka.yml by prefixing all link/joint names for left_/right_ arms.
# Run in container (PyYAML available):
#   docker exec neural-sdf-v2 bash -c "cd ~/ws/neural_sdf/curobo_v2 && \
#       python relpose_experiments/gen_dual_fr3_yml.py"
from pathlib import Path

import yaml

ROOT = Path(__file__).resolve().parent.parent
SRC = ROOT / "curobo/content/configs/robot/franka.yml"
DST = ROOT / "curobo/content/configs/robot/dual_fr3.yml"

ARM_LINKS = [
    "panda_link0", "panda_link1", "panda_link2", "panda_link3", "panda_link4",
    "panda_link5", "panda_link6", "panda_link7", "panda_hand",
    "panda_leftfinger", "panda_rightfinger",
]
ARM_DEFAULT = [0.0, -1.3, 0.0, -2.5, 0.0, 1.5, 0.8]  # 7 arm joints
FR3_ACCEL = [15.0, 7.5, 10.0, 12.5, 15.0, 20.0, 20.0]  # joint1..7 rad/s^2
SIDES = ["left_", "right_"]


def prefix_keep_arm(d):
    """Filter a {link: x} dict to arm links only, emit prefixed per side."""
    out = {}
    for s in SIDES:
        for k, v in d.items():
            if k in ARM_LINKS:
                out[s + k] = v
    return out


def prefix_ignore(ign):
    out = {}
    for s in SIDES:
        for k, vlist in ign.items():
            if k not in ARM_LINKS:
                continue
            out[s + k] = [s + x for x in vlist if x in ARM_LINKS]
    return out


def main():
    src = yaml.safe_load(open(SRC))["robot_cfg"]["kinematics"]

    kin = {
        "format_version": 2.0,
        "asset_root_path": "robot/franka_description",
        "urdf_path": "robot/franka_description/dual_fr3.urdf",
        "base_link": "world_base_link",
        "tool_frames": ["left_panda_hand", "right_panda_hand"],
        "collision_link_names": [s + l for s in SIDES for l in ARM_LINKS],
        "collision_sphere_buffer": 0.0,
        "use_global_cumul": True,
        "mesh_link_names": None,
        "collision_spheres": prefix_keep_arm(src["collision_spheres"]),
        "self_collision_ignore": prefix_ignore(src["self_collision_ignore"]),
        "self_collision_buffer": prefix_keep_arm(src["self_collision_buffer"]),
        "lock_joints": {
            s + f"panda_finger_joint{i}": 0.04 for s in SIDES for i in (1, 2)
        },
        "cspace": {
            "joint_names": [s + f"panda_joint{i}" for s in SIDES for i in range(1, 8)],
            "default_joint_position": ARM_DEFAULT * 2,
            "max_acceleration": FR3_ACCEL * 2,
            "max_jerk": [500.0] * 14,
            "null_space_weight": [1.0] * 14,
            "cspace_distance_weight": [1.0] * 14,
        },
    }
    out = {"robot_cfg": {"kinematics": kin}}
    with open(DST, "w") as f:
        f.write("## Auto-generated by relpose_experiments/gen_dual_fr3_yml.py\n")
        f.write("## Dual Franka Research 3 (Panda geometry + FR3 limits), 0.92 m apart in Y.\n")
        yaml.safe_dump(out, f, sort_keys=False, default_flow_style=None, width=120)
    print(f"wrote {DST}")
    print("joints:", kin["cspace"]["joint_names"])


if __name__ == "__main__":
    main()
